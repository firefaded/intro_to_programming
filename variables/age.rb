# Write a program called age.rb that asks a user how old they are and then tells them how old they will be 
# in 10, 20, 30 and 40 years.

puts "How old are you?"
# asks for user input and converts to an integer
age = gets.chomp.to_i

puts "In 10 years you will be:"
# adds 10 to the age variable
puts age + 10

puts "In 20 years you will be:"
# adds 20 to the age variable
puts age + 20

puts "In 30 years you will be:"
# adds 30 to the age variable
puts age + 30

puts "In 40 years you will be:"
# adds 40 to the age variable
puts age + 40